package com.ceva.base.common.bean;

public class TwoWaySMSBean {

	public String sno;
	public String requestedDate;
	public String requestedFrom;
	public String requestedText;
	public String requestedTo;
	public String getSno() {
		return sno;
	}
	public void setSno(String sno) {
		this.sno = sno;
	}
	public String getRequestedDate() {
		return requestedDate;
	}
	public void setRequestedDate(String requestedDate) {
		this.requestedDate = requestedDate;
	}
	public String getRequestedFrom() {
		return requestedFrom;
	}
	public void setRequestedFrom(String requestedFrom) {
		this.requestedFrom = requestedFrom;
	}
	public String getRequestedText() {
		return requestedText;
	}
	public void setRequestedText(String requestedText) {
		this.requestedText = requestedText;
	}
	public String getRequestedTo() {
		return requestedTo;
	}
	public void setRequestedTo(String requestedTo) {
		this.requestedTo = requestedTo;
	}
	
	
	
}
